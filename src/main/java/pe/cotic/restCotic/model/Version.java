package pe.cotic.restCotic.model;

public class Version {
	
	int versionUsuarios;
	String versionAplicacionVigente;
	int versionPortafolio;
	int versionNivel;
	
	public int getVersionUsuarios() {
		return versionUsuarios;
	}
	public void setVersionUsuarios(int versionUsuarios) {
		this.versionUsuarios = versionUsuarios;
	}
	public String getVersionAplicacionVigente() {
		return versionAplicacionVigente;
	}
	public void setVersionAplicacionVigente(String versionAplicacionVigente) {
		this.versionAplicacionVigente = versionAplicacionVigente;
	}
	public int getVersionPortafolio() {
		return versionPortafolio;
	}
	public void setVersionPortafolio(int versionPortafolio) {
		this.versionPortafolio = versionPortafolio;
	}
	public int getVersionNivel() {
		return versionNivel;
	}
	public void setVersionNivel(int versionNivel) {
		this.versionNivel = versionNivel;
	}
		
}