package pe.cotic.restCotic.bean;

public class CambiarClave {
	
	String usuario;
	String valorCero;
	String valorUno;
	int retorno;
	
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getValorCero() {
		return valorCero;
	}
	public void setValorCero(String valorCero) {
		this.valorCero = valorCero;
	}
	public String getValorUno() {
		return valorUno;
	}
	public void setValorUno(String valorUno) {
		this.valorUno = valorUno;
	}
	public int getRetorno() {
		return retorno;
	}
	public void setRetorno(int retorno) {
		this.retorno = retorno;
	}
	
}
